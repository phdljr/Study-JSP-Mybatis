DELIMITER $$
CREATE PROCEDURE PR_STUDENT (
    PARAM_NUM INTEGER
)
BEGIN

    SELECT *
        FROM student
        WHERE id = PARAM_NUM;

END $$
DELIMITER ;

DELIMITER $$
CREATE PROCEDURE PR_STUDENT_NAME (
    PARAM_NAME VARCHAR(10)
)
BEGIN

    SELECT *
        FROM student
        WHERE nickname = PARAM_NAME;

END $$
DELIMITER ;